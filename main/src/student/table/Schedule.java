package student.table;

/**
 * 说明：课程表
 */
public class Schedule {
    private String SchNo;
    private String SchName;
    private String SchHour;
    private String SchBook;

    public String getSchNo() { return SchNo; }
    public void setSchNo(String schno) { this.SchNo = schno; }

    public String getSchName() { return SchName; }
    public void setSchName(String schname) { this.SchName = schname; }

    public String getSchHour() { return SchHour; }
    public void setSchHour(String schhour) { this.SchHour = schhour; }

    public String getSchBook() { return SchBook; }
    public void setSchBook(String schbook) {this.SchBook = schbook; }

}
